def movePro():
    """
    Moves Karel one step and updates the global position variables (x, y).
    """
    global x, y
    move()
    if facing_east():
        x += 1
    elif facing_west():
        x -= 1
    elif facing_north():
        y += 1
    elif facing_south():
        y -= 1

# --- Vertical line drawing function ---
def khogenMultiVert(posX, posY, length, colors):
    global x, y
    
    start_y = posY
    intended_end_y = posY + length

    dist_to_start = abs(x - posX) + abs(y - start_y)
    dist_to_end = abs(x - posX) + abs(y - intended_end_y)

